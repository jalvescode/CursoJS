let nome = 'Joaquim'; // Variável declarada com valor
let data; // Variável declarada sem valor
data = 1984; // Inicializando a variável
console.log(nome, 'nasceu em', data);
console.log('em 2000', nome, 'conheceu Maria.');
data = 2012; // Modificando o valor da variável
console.log(nome, 'casou-se com Maria em', data);
console.log('Maria teve 1 filho com', nome, 'em 2015');
console.log('O filho de', nome, 'se chama Eduardo');

